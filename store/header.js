export const mutations = {
  openMenu (state, data) {
    state.mainMenuRight.forEach(item => {
      if (item.id === data.id) {
        item.opened = data.opened
      }
    })
  }
}

export const getters = {
  getRightMenu: state => {
    let result = state.mainMenuRight
    result.forEach(item => {
      if (item.childrens) {
        item.opened = false
      }
    })
    return result
  }
}
